def fill_translations(input_path: str, output_path: str):
    """Fill Vietnamese translations in the JSON file."""
    
    with open(input_path, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    # Track statistics
    filled = 0
    unfilled = 0
    
    # Process each category
    for category_name, patterns in data.get('categories', {}).items():
        for pattern in patterns:
            zh = pattern.get('zh', '')
            if zh in TRANSLATIONS:
                vn_correct, vn_wrong = TRANSLATIONS[zh]
                pattern['vn_correct'] = vn_correct
                pattern['vn_wrong'] = vn_wrong
                filled += 1
            else:
                # Mark unfilled patterns
                if not pattern.get('vn_correct'):
                    pattern['vn_correct'] = f"[TODO: {zh}]"
                    pattern['vn_wrong'] = ""
                    unfilled += 1
    
    # Update metadata
    data['metadata']['version'] = '1.1'
    data['metadata']['description'] = 'Vietnamese translations for Japanese light novel kanji patterns'
    
    # Write output
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    
    print(f"✓ Translations filled: {filled}")
    print(f"✗ Still need translation: {unfilled}")
    print(f"✓ Output saved to: {output_path}")
    
    return filled, unfilled


